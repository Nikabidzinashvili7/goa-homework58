#  შექმენით ფუნქცია, რომელსაც გადასცემთ რიცხვების სიას.
#  გადაუარეთ გადმოცემულ სიას for ციკლით და დააბრუნეთ ახალი სია, 
#  სადაც ჩამატებული იქნება გადმოცემული სიიდან მხოლოდ ის რიცხვები, რომლებიც მეტია 10-ზე. საბოლოოდ დააბრუნეთ ეს სია




def lst(numbers):
    new_lst = []
    for i in numbers:
        if i > 10:
            new_lst.append(i)
    return new_lst


numbers = [1,2,33,4,50,7,11,22,333,44,5566,78]

result = lst(numbers)
print(result)




