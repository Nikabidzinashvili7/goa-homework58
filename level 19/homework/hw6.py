# შექმენით სია, სადაც გექნებათ 1-ანები და 0-ანები, შექმენით ახალი სია, რომელიც თავიდან იქნება ცარიელი.
# for loop-ით გადაუარეთ პირველ სიას და თუ საიტერაციო ცვლადის მნიშვნელობა იქნება 1,
# ახალ სიაში ჩაამატეთ True, სხვა შემთხვევაში False. საბოლოოდ დაბეჭდეთ ახალი სია

numbers = [0,1,0,1,0,1,1,0]

lst = []

for i in numbers:
    if i == 1:
        lst.append(True)
    else:
        lst.append(False)

print(lst)
