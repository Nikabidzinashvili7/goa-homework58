#მეხუთე დავალების მსგავსად, შემოატანინეთ მომხმარებელს ტემპერატურა, თუმცა ამჯერად ფარენგეიტში. შემდეგ კი დაწერეთ პროგრამა, რომელიც შემოტანილ ტემპერატურას ცელსიუსში გადაიყვანს და საბოლოოდ დაბეჭდეთ შედეგი.
#ტემპერატურის შეყვანა ფარენგეიტებში
fahrenheit = int(input("enter temperature in fahrenheit:"))

#გადაგვყავს ცელსიუსებში
celsius = (fahrenheit - 32) * 5/9

#შედეგი
print(f"temperature in {celsius}°C")


